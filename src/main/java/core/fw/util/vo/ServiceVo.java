package core.fw.util.vo;

public class ServiceVo {
	
	private String className;
	
	private String methodName;
	
	private String inputVoName;
	
	private String outputVoName;

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getMethodName() {
		return methodName;
	}

	public void setMethodName(String methodName) {
		this.methodName = methodName;
	}

	public String getInputVoName() {
		return inputVoName;
	}

	public void setInputVoName(String inputVoName) {
		this.inputVoName = inputVoName;
	}

	public String getOutputVoName() {
		return outputVoName;
	}

	public void setOutputVoName(String outputVoName) {
		this.outputVoName = outputVoName;
	}

	

}
